#include "libft.h"

int	ft_str_isempty(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if (str[i] != '\n' && str[i] != '\t' && str[i] != ' ')
			return (0);
		i++;
	}
	return (1);
}
